
/*Write a for loop to calculate the factorial of a number n (e.g., 5! = 120). */
public class hw {
    public static void main(String[]args){
        int n = 5;
        int mul = 0;
      //  int i;
        for(int i=n;i<=n;i--){
            mul = n*(n-i);
        }
        System.out.println(mul);
    }
    
}
